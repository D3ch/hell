{
	return;
	if(minerDepth >= paidMinerNames[0].depth && minerDepth <= paidMinerNames[paidMinerNames.length - 1].depth)
	{
		for(var i = 0; i < paidMinerNames.length; i++)
		{
			if(paidMinerNames[i].depth == minerDepth && paidMinerNames[i].workerIndex == minerIndex)
			{
				return paidMinerNames[i].name;
			}
		}
	}
	return null;
}

